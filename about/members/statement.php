<p>
As a member of the CFD Toolkit Consortium I support the principle of an open, community-driven computational infrastructure for relativistic astrophysics that contributes to advancing science and educating the next generation of researchers in a sustainable, cost-effective manner. 
</p>

<h3>
As a user of the CFD Toolkit I will
</h3>

<ol>
<li> Acknowledge use of the  CFD Toolkit in publications and presentations following the <a href="#citation">CFD Toolkit citation guidelines</a>.</li>

<li> Report to the CFD Toolkit Maintainers any identified bugs or patches to the CFD Toolkit.</li>
</ol>

<h3>
As a contributor to the CFD Toolkit I will
</h3>

<ol>
<li> Ensure that software components are reliably accessible for anonymous users through the CFD Toolkit distribution mechanism.</li>

<li> Distribute contributed components under an open source software license agreed on with the CFD Toolkit Consortium</li>

<li> Collaborate with the CFD Toolkit Maintainers on releases.</li>

<li> Report to the CFD Toolkit Maintainers any bugs identified in the contributed components.</li>

<li> Where feasible, add patches, bug fixes, and feature requests to software components as requested by the CFD Toolkit Maintainers following usual development procedures.</li>

<li> Release contributed components to the CFDToolkit.org repository for distribution and development if I am unable to continue to host software or am no longer actively supporting or using the software.</li>

<li> Ensure that all stakeholders of contributed components are cognizant of and agree with these conditions.</li>
</ol>

<h3>Citation Guidelines</h3>

The CFD Toolkit citation guidelines are designed to appropriately credit individuals who have made intellectual contributions to the toolkit in designing the algorithms and software implementations that contribute substantially to provision of an effective framework.

